function performSearch() {
    const searchTerm = document.getElementById('searchInput').value.trim().toLowerCase();
    
    if (!searchTerm) {
        alert('–í–≤–µ–¥–∏—Ç–µ –ø–æ–∏—Å–∫–æ–≤—ã–π –∑–∞–ø—Ä–æ—Å');
        return;
    }
    
    // –ü–æ–∏—Å–∫ –ø–æ —Å—Ç–∞—Ç—å—è–º
    searchResults = [];
    
    for (const [articleId, article] of Object.entries(wikiArticles)) {
        const searchContent = (
            article.title.toLowerCase() + ' ' + 
            article.content.toLowerCase() + ' ' + 
            article.tags.join(' ')
        );
        
        if (searchContent.includes(searchTerm)) {
            searchResults.push({
                id: articleId,
                article: article,
                relevance: calculateRelevance(article, searchTerm)
            });
        }
    }
    
    // –°–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ –ø–æ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç–∏
    searchResults.sort((a, b) => b.relevance - a.relevance);
    
    // –ü–æ–∫–∞–∑–∞—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
    showSearchResults(`–†–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø–æ–∏—Å–∫–∞: "${searchTerm}"`, searchResults);
}

// –ü–æ–∏—Å–∫ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
function showSearchResultsForCategory(categoryName) {
    let results = [];
    let title = '–í—Å–µ —Å—Ç–∞—Ç—å–∏';
    
    if (categoryName !== 'all') {
        for (const [articleId, article] of Object.entries(wikiArticles)) {
            if (article.category === categoryName) {
                results.push({
                    id: articleId,
                    article: article,
                    relevance: 100
                });
            }
        }
        title = getCategoryName(categoryName);
    } else {
        // –í—Å–µ —Å—Ç–∞—Ç—å–∏
        for (const [articleId, article] of Object.entries(wikiArticles)) {
            results.push({
                id: articleId,
                article: article,
                relevance: 100
            });
        }
    }
    
    showSearchResults(title, results);
}

// –ü–æ–∫–∞–∑–∞—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø–æ–∏—Å–∫–∞
function showSearchResults(title, results) {
    document.getElementById('welcomeScreen').classList.add('hidden');
    document.getElementById('articleScreen').classList.add('hidden');
    document.getElementById('searchResultsScreen').classList.remove('hidden');
    
    // –£—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –∑–∞–≥–æ–ª–æ–≤–æ–∫
    document.getElementById('searchResultsTitle').textContent = title;
    
    // –û—Ç–æ–±—Ä–∞–∑–∏—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
    const resultsContainer = document.getElementById('searchResults');
    resultsContainer.innerHTML = '';
    
    if (results.length === 0) {
        resultsContainer.innerHTML = `
            <div class="no-results">
                <i class="fas fa-search"></i>
                <h4>–ù–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ</h4>
                <p>–ü–æ–ø—Ä–æ–±—É–π—Ç–µ –∏–∑–º–µ–Ω–∏—Ç—å –ø–æ–∏—Å–∫–æ–≤—ã–π –∑–∞–ø—Ä–æ—Å –∏–ª–∏ –≤—ã–±—Ä–∞—Ç—å –¥—Ä—É–≥—É—é –∫–∞—Ç–µ–≥–æ—Ä–∏—é</p>
            </div>
        `;
        return;
    }
    
    results.forEach(result => {
        const article = result.article;
        const resultElement = document.createElement('div');
        resultElement.className = 'search-result-item';
        resultElement.onclick = () => showArticle(result.id);
        
        resultElement.innerHTML = `
            <h4>${article.title}</h4>
            <p>${getArticleExcerpt(article.content)}</p>
            <div class="search-result-meta">
                <span class="difficulty ${article.difficulty}">${getDifficultyName(article.difficulty)}</span>
                <span><i class="fas fa-clock"></i> ${article.readTime} –º–∏–Ω</span>
                <span><i class="fas fa-tag"></i> ${getCategoryName(article.category)}</span>
            </div>
        `;
        
        resultsContainer.appendChild(resultElement);
    });
}

// –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏
function calculateRelevance(article, searchTerm) {
    let relevance = 0;
    
    // –ü—Ä–æ–≤–µ—Ä–∫–∞ –≤ –∑–∞–≥–æ–ª–æ–≤–∫–µ (–≤—ã—Å—à–∏–π –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    if (article.title.toLowerCase().includes(searchTerm)) {
        relevance += 100;
    }
    
    // –ü—Ä–æ–≤–µ—Ä–∫–∞ –≤ —Ç–µ–≥–∞—Ö
    if (article.tags.some(tag => tag.toLowerCase().includes(searchTerm))) {
        relevance += 50;
    }
    
    // –ü—Ä–æ–≤–µ—Ä–∫–∞ –≤ –∫–æ–Ω—Ç–µ–Ω—Ç–µ
    if (article.content.toLowerCase().includes(searchTerm)) {
        relevance += 10;
    }
    
    return relevance;
}

function getArticleExcerpt(content, length = 150) {
    // –£–¥–∞–ª—è–µ–º HTML —Ç–µ–≥–∏ –∏ –ø–æ–ª—É—á–∞–µ–º —á–∏—Å—Ç—ã–π —Ç–µ–∫—Å—Ç
    const text = content.replace(/<[^>]*>/g, '');
    return text.length > length ? text.substring(0, length) + '...' : text;
}

